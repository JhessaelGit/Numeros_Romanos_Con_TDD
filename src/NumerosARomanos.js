function ConvertirARomanos(numero)
{
    if(numero === 1)
        return "I";
    else if(numero === 2)
        return "II";
    else if(numero === 3)
        return "III";
    else if(numero === 4)
        return "IV";
    else if(numero === 5)
        return "V";
    else if(numero === 6)
        return "VI";
    else if(numero === 7)
        return "VII";
}

export default ConvertirARomanos;